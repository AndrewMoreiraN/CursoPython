"""
str - string
"""
print('Essa é uma \'string\' (str).')
# \ para ignorar o próximo char nao imprimindo a \
print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')
print('Esse é meu \n texto')
# \n para quebra de linhas
print(r'Esse é meu \n texto')
# r para nao executar nenhum comando dentro da string
